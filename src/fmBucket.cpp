#include "fmDM.h"
//----------------------------------------------------------------------------------------------------------------------//
fmBucket_C::fmBucket_C()
{
    m_nMax = 0;
}
fmBucket_C::~fmBucket_C()
{
    this->clear();
}
//----------------------------------------------------------------------------------------------------------------------//
list<fmNode_C*>& fmBucket_C::getNeg(int p_nId)
{
    return m_vNegative[p_nId];
}
//----------------------------------------------------------------------------------------------------------------------//
list<fmNode_C*>& fmBucket_C::getPos(int p_nId)
{
    return m_vPositive[p_nId];
}
//----------------------------------------------------------------------------------------------------------------------//
void fmBucket_C::clear()
{
    m_vPositive.clear();
    m_vNegative.clear();
}
//----------------------------------------------------------------------------------------------------------------------//
void fmBucket_C::setSize(int n)
{
    m_nMax = n;
    m_vPositive.resize(n+1);
    m_vNegative.resize(n+1);
}
